
numero1 = input("digite um numero: ")
numero1 = int(numero1)

numero2 = input("digite um segundo numero: ")
numero2 = int(numero2)

if(numero1==numero2):#operador de igualdade
    print("o numero %d é iagual a %d. "%(numero1, numero2))
if(numero1!=numero2):#operador de diferença
    print("o numero %d é diferente de %d. "%(numero1, numero2))
if(numero1<numero2):#operador menor que.
    print("o numero %d é menor que o %d. "%(numero1, numero2))
if(numero1>numero2):#operador maior que.
    print("o numero %d é maior que o %d. "%(numero1, numero2))
if(numero1>=numero2):#operador maior ou igual que.
    print("o numero %d é maior ou igual que %d. "%(numero1, numero2))
if(numero1<=numero2):#operador menor ou igual que.
    print("o numero %d é menor ou igual a %d. "%(numero1, numero2))
